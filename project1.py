a=float(input('Enter 1st number '))
b=float(input('Enter 2nd number '))
s=input('Enter operator ')
if s =='+':
    print('The answer is ',a+b)
elif s =='-':
    print('The answer is ',a-b)
elif s =='/':
    print('The answer is ',a/b)
elif s =='*':
    print('The answer is ',a*b)
elif s =='//':
    print('The answer is ',a//b)
elif s =='%':
    print('The answer is ',a%b)